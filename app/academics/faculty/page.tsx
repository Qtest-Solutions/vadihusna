  if (designation === "PRT") return "bg-yellow-100 text-yellow-800";
  if (designation.includes("PET")) return "bg-red-100 text-red-800";
  if (designation === "Librarian") return "bg-indigo-100 text-indigo-800";
  if (designation === "COUNSELLOR") return "bg-pink-100 text-pink-800";
  return "bg-gray-100 text-gray-800";
